

public class RecycleBin extends Receptacle {
	public RecycleBin(int xLoc, int yLoc, int rWidth, int rHeight) {
		this.xLocation = xLoc;
		this.yLocation = yLoc;
		this.width = rWidth;
		this.height = rHeight;
		this.setRelativeCollisionRect(0, 0, rWidth, rHeight);
	}
		
	
}

